    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        databaru = false;
        int row = jTable1.getSelectedRow();
        String tabel_klik =(jTable1.getModel().getValueAt(row, 0).toString());
        cl.setNo(tabel_klik);
        try
        {           
                ResultSet rs = cl.ViewById();
                if(rs.next())
                {
                    cl.setNo(rs.getString("no"));
                    cl.setTanggal(rs.getString("tanggal"));
                    cl.setNama(rs.getString("nama"));
                    cl.setUkuran(rs.getString("ukuran"));
                    cl.setJumlah(Integer.parseInt(rs.getString("jumlah")));
                    cl.setHarga(Double.valueOf(rs.getString("harga")));
                    cl.setDiskon(Double.valueOf(rs.getString("diskon")));
                    cl.setJmlBayar(Double.valueOf(rs.getString("jumlahbayar")));
                    
                    this.txtno.setText(cl.getNo());
                    this.txttanggal.setText(cl.getTanggal());
                    this.txtnama.setText(cl.getNama());
                    this.cmbukuran.setSelectedItem(cl.getUkuran());
                    this.txtjml.setText(Integer.toString(cl.getJumlah()));
                    this.txtharga.setText(String.format("%.2f",cl.getHarga()));
                    this.txtdiskon.setText(String.format("%.2f",cl.getDiskon()));
                    this.txtjmlbayar.setText(String.format("%.2f",cl.getJmlBayar()));
                   
                    this.txtno.setEditable(false);
                }
        }catch(SQLException ex)
                {
                    Logger.getLogger(frmpenjualan.class.getName()).log(Level.SEVERE, null, ex);
                }
    }//GEN-LAST:event_jTable1MouseClicked

