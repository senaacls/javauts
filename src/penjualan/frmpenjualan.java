    private void GetData()  throws SQLException
    {
        Object[] kolom = {"No", "Tanggal", "Nama", "Ukuran", "Jumlah", "Harga", "Diskon", "JumlahBayar"};
        DefaultTableModel tabmode = new DefaultTableModel(null, kolom);
        ResultSet rs = cl.ViewByAll();
        while(rs.next())
        {
            String data[]={rs.getString("no"),rs.getString("tanggal"),rs.getString("nama"),rs.getString("ukuran"), rs.getString("jumlah"), rs.getString("harga"), rs.getString("diskon"), rs.getString("jumlahbayar")};
            tabmode.addRow(data);
        }
        jTable1.setModel(tabmode);
    }

    

