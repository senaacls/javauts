    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
            cl.setNo(txtno.getText());
            cl.setTanggal(txttanggal.getText());
            cl.setNama(txtnama.getText());
            cl.setUkuran(cmbukuran.getSelectedItem().toString());
            cl.setJumlah(Integer.parseInt(txtjml.getText()));
            cl.setHarga(Double.valueOf(txtharga.getText()));
            cl.setDiskon(Double.valueOf(txtdiskon.getText()));
            cl.setJmlBayar(Double.valueOf(txtjmlbayar.getText()));
            boolean h = false;
            try
            {
                if(databaru == true)
                {
                    h = cl.Insert();
                }
                else
                {
                    h = cl.Update();
                }
                JOptionPane.showMessageDialog(null,"Penyimpanan berhasil");
                this.GetData();
                clearText(this.getContentPane());
                
            } catch(SQLException ex)
            {
                JOptionPane.showMessageDialog(null, "Penyimpanan gagal" + ex.getMessage());
            }
    }//GEN-LAST:event_btnsimpanActionPerformed

