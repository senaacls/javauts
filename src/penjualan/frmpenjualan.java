   private void jumlahbayar(){
        double jml, harga;
        double diskon = 0;
        double total  = 0;
        jml         = Double.valueOf(txtjml.getText());
        harga       = Double.valueOf(txtharga.getText());
        
        if(jml >= 5)
        {
            diskon = (harga * 10) / 100;
        }
        else
        {
            diskon = 0;
        }
        
        this.txtdiskon.setText(String.format("%.2f",diskon));
        total       = jml * harga - diskon;
        this.txtjmlbayar.setText(String.format("%.2f",total));
    }
   
   private void resize()
   {
       String size = this.cmbukuran.getSelectedItem().toString();
       double harga   = 0;
       
       if(size.equals("S"))
       {
           harga = 60000;
       }
       
       else if(size.equals("M"))
       {
           harga = 70000;
       }
       
       else if(size.equals("L"))
       {
           harga = 80000;
       }
       
       else if(size.equals("XL"))
       {
           harga = 100000;
       }
       
       txtharga.setText(String.format("%.2f", harga));
   }
   
 
